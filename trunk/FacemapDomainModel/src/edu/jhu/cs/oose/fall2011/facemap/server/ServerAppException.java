package edu.jhu.cs.oose.fall2011.facemap.server;

public class ServerAppException extends Exception {

	public ServerAppException() {
		super();
	}

	public ServerAppException(String message, Throwable cause) {
		super(message, cause);
	}

	public ServerAppException(String message) {
		super(message);
	}

	public ServerAppException(Throwable cause) {
		super(cause);
	}

}
